    if bil == 11:
        print(bil," adalah bilangan prima")
    elif bil == 13:
        print(bil, " adalah bilangan prima")
    elif bil == 17:
        print(bil, " adalah bilangan prima")
    elif bil == 19:
        print(bil, " adalah bilangan prima")
    elif bil == 23:
        print(bil, " adalah bilangan prima")
    else:
        print(bil," bukan prima")
